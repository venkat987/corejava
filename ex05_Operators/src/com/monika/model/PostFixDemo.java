package com.monika.model;

public class PostFixDemo {
	public static void main(String[] arg) {
		int a = 100;
		System.out.println(a);
		a++;
		System.out.println(a);
		System.out.println(a++);
		System.out.println(a);
		
		int b= 200;
		System.out.println(b);
		b--;
		System.out.println(b);
		System.out.println(b--);
		System.out.println(b);
		
	}
}
