package com.monika.model;

public class AssignmentDemo {
	 public static void main(String[] arg) {
		 int a = 400; // The vale of 400 is assigned to a using (Assignment operator'=')
		 System.out.println(a);
		 
		 a = a+100;
		 System.out.println(a);
		 
		 a += 100;
		 System.out.println(a);
		 
		 a -= 100;
		 System.out.println(a);
		 
		 a *= 5;
		 System.out.println(a);
		 
		 a /= 100;
		 System.out.println(a);
		 
	 }
}
